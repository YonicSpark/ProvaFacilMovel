public class Prova {
    private int proCodigo;
    private String proData;
    private String proDisciplina;
    private String proCurso;

    public int getProCodigo() {
        return proCodigo;
    }

    public void setProCodigo(int proCodigo) {
        this.proCodigo = proCodigo;
    }

    public String getProData() {
        return proData;
    }

    public void setProData(String proData) {
        this.proData = proData;
    }

    public String getProDisciplina() {
        return proDisciplina;
    }

    public void setProDisciplina(String proDisciplina) {
        this.proDisciplina = proDisciplina;
    }

    public String getProCurso() {
        return proCurso;
    }

    public void setProCurso(String proCurso) {
        this.proCurso = proCurso;
    }
}
